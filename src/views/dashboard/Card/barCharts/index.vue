<template>
  <div class="charts" ref="charts">

  </div>
</template>

<script>
    import * as echarts from 'echarts';
    export default {
        name: "index",
        mounted(){
          //初始化echarts实例
          let lineCharts = echarts.init(this.$refs.charts)

          lineCharts.setOption({
            xAxis: {
              //隐藏x轴
              show: false,
              //均分
              type: "category",
            },
            yAxis: {
              //隐藏y轴
              show: false,
            },
            series:[
              {
                type:'bar',
                data:[10,19,22,34,24,56]
              }
            ],
            //布局调试
            grid: {
              left: 0,
              top: 0,
              right: 0,
              bottom: 0,
            },

            //提示
            tooltip:{}
          })
        }
    }
</script>

<style scoped>
  .charts{
    width: 100%;
    height: 100%;
  }
</style>
